def montrer_robot(carte, pos_robot):
    carte_avec_robot = carte[:]
    carte_avec_robot[pos_robot[0]][pos_robot[1]] = '+'
    montrer_carte(carte_avec_robot)

def montrer_chemin(carte, chemin):
    carte_avec_chemin = carte[:]
    for pos in chemin:
        carte_avec_chemin[:]
        carte_avec_chemin[pos[0]][pos[1]] = '.'
    montrer_carte(carte_avec_chemin)
